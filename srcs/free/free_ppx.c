#include "pipex.h"

void	free_ppx(t_ppx *ppx)
{
	ppx->ac = 0;
	ppx->av = 0;
	ppx->envp = NULL;
	ppx->cmd_cnt = 0;
	ppx->infile = 0;
	ppx->outfile = 0;
	ppx->pipe = NULL;
}
